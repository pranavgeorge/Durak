namespace Durak
{
    /// <summary>
    /// The Rank enumeration provides a list of all of the valid ranks (values)
    /// for the cards in the program. The ACE has value 1, ensuring that it is
    /// easy to map cards to related numeric values.
    /// </summary>

    public enum CardRank
    {
        /// <summary>
        /// Card that represent the digit 1
        /// </summary>
        Ace = 1,

        /// <summary>
        /// Card that represent the digit 2
        /// </summary>
        Deuce,

        /// <summary>
        /// Card that represent the digit 3
        /// </summary>
        Three,

        /// <summary>
        /// Card that represent the digit 4
        /// </summary>
        Four,

        /// <summary>
        /// Card that represent the digit 5
        /// </summary>
        Five,

        /// <summary>
        /// Card that represent the digit 6
        /// </summary>
        Six,

        /// <summary>
        /// Card that represent the digit 7
        /// </summary>
        Seven,

        /// <summary>
        /// Card that represent the digit 8
        /// </summary>
        Eight,

        /// <summary>
        /// Card that represent the digit 9
        /// </summary>
        Nine,

        /// <summary>
        /// Card that represent the digit 10
        /// </summary>
        Ten,

        /// <summary>
        /// Card that represent the digit 11
        /// </summary>
        Jack,

        /// <summary>
        /// Card that represent the digit 12
        /// </summary>
        Queen,

        /// <summary>
        /// Card that represent the digit 13
        /// </summary>
        King
    }
}
